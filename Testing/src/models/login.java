    public void login(){
        
        
        String username = txtuser.getText();
        String password = txtpass.getText();
        
        
        try {
            Connection conl = createConnection();
            ptsmnt = conl.prepareStatement(GET_NAME_AND_PASS);
            ptsmnt.setString(1, username);
            ptsmnt.setString(2, password);
            rs = ptsmnt.executeQuery();
            
            if(rs.next())
            {
                String isAdmin = rs.getString("isAdmin");
                String Active = rs.getString("status");
                if(Active.equals("Active"))
                {   
                    this.hide();
                    new main(txtuser.getText(),isAdmin,txtpass.getText()).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, ACCT_NOT_ACTIVE);
                    txtuser.setText("");
                    txtpass.setText("");
                    txtuser.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(this, USER_PASS_INVALID);
                txtuser.setText("");
                txtpass.setText("");
                txtuser.requestFocus();
            } 
            
            
            
            
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this,SYS_ERROR);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,SYS_ERROR);
        } 
       
        
        
    }
    
    
    
    
