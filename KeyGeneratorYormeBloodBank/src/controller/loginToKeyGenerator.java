    public void login(){
        
        
        String devUser = txtuser.getText();
        String devPass = txtpass.getText();
        
        
        
        try {
            Connection conl = createConnection();
            PreparedStatement ptsmnt;
            ResultSet rs;
            ptsmnt = conl.prepareStatement(DEV_LOGIN);
      
            ptsmnt.setString(1, devUser);
            ptsmnt.setString(2, devPass);
            rs = ptsmnt.executeQuery();
            
            if(rs.next()){
                    conl.close();
                    this.hide();
                    new ControlPanelDev().setVisible(true);
                    this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, ADMIN_WRONG_CREDENTIALS);
                txtuser.setText("");
                txtpass.setText("");
                txtuser.requestFocus();
            } 
             conl.close();
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this,SYS_ERROR);
        } 

    }
    
    
    
    
