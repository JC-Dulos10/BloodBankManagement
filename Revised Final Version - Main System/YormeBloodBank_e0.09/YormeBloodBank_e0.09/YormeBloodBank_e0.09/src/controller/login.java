    public void login(){
        
        
        String username = txtuser.getText();
        String password = txtpass.getText();
        
        
        //String passwordCheckHolder = password;
        
        try {
            Connection conl = createConnection();
            PreparedStatement ptsmnt;
            ResultSet rs;
            ptsmnt = conl.prepareStatement(LOGIN);
            ptsmnt.setString(1, username);
//            Encrypt password and compare to encrypted password on db
            password = encrypt(password);
            ptsmnt.setString(2, password);
            
           
            
            rs = ptsmnt.executeQuery();
            
            if(rs.next())
            {
                String isAdmin = rs.getString("isAdmin");
                String Active = rs.getString("status");
                int bloodBank_id = rs.getInt("bloodBank_id");
                int userID = rs.getInt("id");
                if(Active.equals("Active"))
                {   
                    conl.close();
                    this.hide();
                    new main(txtuser.getText(),isAdmin,txtpass.getText(),bloodBank_id, userID).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, ACCT_NOT_ACTIVE);
                    txtuser.setText("");
                    txtpass.setText("");
                    txtuser.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(this, USER_PASS_INVALID);
                txtuser.setText("");
                txtpass.setText("");
                txtuser.requestFocus();
            } 
            conl.close();
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this,SYS_ERROR);
        } 

    }
    
    
    
    
